<h1>Dynamic Routes</h1>
<p>We can wrap file name with '[]', for example, [id].svelte, this is called dynamic route, "id" is dynamic, we can read dynamic route on server or client.</p>
<p>We can get dynamic route on server side by deconstructing conext object to get params object</p>
<p>We can also read dynamic route on <a target="_blank" href="https://stackoverflow.com/questions/67141392/access-parameter-without-load-function-in-svelte-kit">client side</a></p>
<p>Add any text after '/demo10/' to enter that specific route</p>
